class Equipo {
  final int id;
  final String nombreEquipo;
  final String marca;
  final String modelo;
  final String serie;
  final String recinto;
  final int estado;

  Equipo({
    required this.id,
    required this.nombreEquipo,
    required this.marca,
    required this.modelo,
    required this.serie,
    required this.recinto,
    required this.estado,
  });
}
